import numpy as np
import matplotlib.pyplot as plt

# ============================================================
# Physical constants
# ============================================================

c = 299792458.0
mu0 = 4e-7 * np.pi
eps0 = 1.0 / (mu0 * c**2)

# ============================================================
# Grid
# ============================================================

Nx = 200
Ny = 200

dx = 1e-3
dy = dx

dt = 0.4 * dx / c

Nt = 1500

# ============================================================
# Plasma parameters
# ============================================================

omega_p = 2*np.pi*5e9
omega_c = 2*np.pi*2e9
nu = 5e8

# ============================================================
# Fields
# ============================================================

Ex = np.zeros((Nx, Ny))
Ey = np.zeros((Nx, Ny))
Hz = np.zeros((Nx, Ny))

Jx = np.zeros((Nx, Ny))
Jy = np.zeros((Nx, Ny))

# ============================================================
# PML conductivity
# ============================================================

pml = 20
sigma_max = 5.0

sigma_x = np.zeros((Nx, Ny))
sigma_y = np.zeros((Nx, Ny))

for i in range(Nx):

    sx = 0.0

    if i < pml:
        sx = sigma_max * ((pml - i)/pml)**2

    elif i >= Nx - pml:
        sx = sigma_max * ((i - (Nx-pml))/pml)**2

    sigma_x[i,:] = sx

for j in range(Ny):

    sy = 0.0

    if j < pml:
        sy = sigma_max * ((pml - j)/pml)**2

    elif j >= Ny - pml:
        sy = sigma_max * ((j - (Ny-pml))/pml)**2

    sigma_y[:,j] = sy

sigma_e = sigma_x + sigma_y

# ============================================================
# Source
# ============================================================

sx = Nx // 2
sy = Ny // 2

def source(t):

    t0 = 80
    spread = 20

    return np.exp(-((t - t0)/spread)**2)

# ============================================================
# Main FDTD loop
# ============================================================

for n in range(Nt):

    # --------------------------------------------------------
    # Update Hz
    # --------------------------------------------------------

    Hz[:-1,:-1] += (dt / mu0) * (
        (Ex[:-1,1:] - Ex[:-1,:-1]) / dy
        -
        (Ey[1:,:-1] - Ey[:-1,:-1]) / dx
    )

    # --------------------------------------------------------
    # Update currents (proper staggered form)
    # --------------------------------------------------------

    a = (1 - nu*dt/2) / (1 + nu*dt/2)
    b = dt * eps0 * omega_p**2 / (1 + nu*dt/2)
    cpl = dt * omega_c / (1 + nu*dt/2)

    Jx_new = a*Jx + b*Ex + cpl*Jy
    Jy_new = a*Jy + b*Ey - cpl*Jx

    Jx = Jx_new
    Jy = Jy_new

    # --------------------------------------------------------
    # PML coefficients
    # --------------------------------------------------------

    ca = (1 - sigma_e*dt/(2*eps0)) / \
         (1 + sigma_e*dt/(2*eps0))

    cb = dt / eps0 / \
         (1 + sigma_e*dt/(2*eps0))

    # --------------------------------------------------------
    # Update Ex
    # --------------------------------------------------------

    Ex[:,1:-1] = (
        ca[:,1:-1] * Ex[:,1:-1]
        +
        cb[:,1:-1] * (
            (Hz[:,1:-1] - Hz[:,:-2]) / dy
            -
            Jx[:,1:-1]
        )
    )

    # --------------------------------------------------------
    # Update Ey
    # --------------------------------------------------------

    Ey[1:-1,:] = (
        ca[1:-1,:] * Ey[1:-1,:]
        -
        cb[1:-1,:] * (
            (Hz[1:-1,:] - Hz[:-2,:]) / dx
            +
            Jy[1:-1,:]
        )
    )

    # --------------------------------------------------------
    # Inject source
    # --------------------------------------------------------

    Ex[sx, sy] += source(n)

    # --------------------------------------------------------
    # Visualization
    # --------------------------------------------------------

    if n % 20 == 0:

        plt.clf()

        field = np.sqrt(Ex**2 + Ey**2)

        plt.imshow(
            field.T,
            origin='lower',
            cmap='RdBu',
            vmin=-0.1,
            vmax=0.1
        )

        plt.title(f"Step {n}")

        plt.pause(0.01)

plt.show()